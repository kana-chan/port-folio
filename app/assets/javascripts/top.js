$(function(){
  $('.topContent').on('mouseover',function(){
      $('.topContent').fadeOut();
      $('.wellcomeBox').fadeIn();
      $('.wellcomeBox__message').fadeIn();
      $('.wellcomeBox__linkBtn').fadeIn();
      $('.wellcomeBtn').fadeIn();
    });
  $('.wellcomeBox').on('mouseleave',function(){
    $('.topContent').fadeIn();
    $('.wellcomeBox').fadeOut();
    $('.wellcomeBox__message').fadeOut();
    $('.wellcomeBox__linkBtn').fadeOut();
    $('.wellcomeBtn').fadeOut();
  });
});
